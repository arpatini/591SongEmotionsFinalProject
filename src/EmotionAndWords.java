
public class EmotionAndWords {
	
	String emotion;
	String word;
	 
	/**
	 * an "emotion and words" constructor is built here; this way the word can be related to a specific emotion
	 * @param emotion
	 * @param word
	 */
	public EmotionAndWords(String emotion, String word) {
		this.emotion = emotion;
		this.word = word;
	}
	
	

}
